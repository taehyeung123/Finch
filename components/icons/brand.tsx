export type BrandApp = "instagram" | "tiktok" | "threads" | "meta";

/*
  앱 아이콘 타일 — 실제 홈스크린 앱 아이콘의 정품 외관을 재현한다.
  사용자 요청(진짜 로고)에 따라 이 타일에 한해 브랜드 원본 색/그라디언트를 쓴다.
  인스타그램은 공식 방사형 그라디언트, 틱톡/쓰레드는 블랙, 메타는 브랜드 블루.
*/
const TILE_GLYPH: Record<BrandApp, (props: { className?: string }) => React.ReactNode> = {
  instagram: InstagramGlyph,
  tiktok: TiktokGlyph,
  threads: ThreadsGlyph,
  meta: MetaGlyph,
};

export function AppIconTile({
  app,
  size = 56,
  className,
}: {
  app: BrandApp;
  size?: number;
  className?: string;
}) {
  const Glyph = TILE_GLYPH[app];

  if (app === "instagram") {
    // 인스타그램 공식 앱 아이콘 — 방사형 그라디언트(CSS) + 흰 카메라.
    // SVG def/id 대신 CSS 그라디언트를 쓴다: 타일이 여러 개일 때 id 충돌이 없어 견고하다.
    return (
      <span
        className={cn("flex shrink-0 items-center justify-center rounded-[22%] text-white", className)}
        style={{
          width: size,
          height: size,
          backgroundImage:
            "radial-gradient(circle at 30% 107%, #FDF497 0%, #FDF497 5%, #FD5949 45%, #D6249F 60%, #285AEB 90%)",
        }}
        aria-hidden
      >
        <Glyph className="size-[62%]" />
      </span>
    );
  }

  const bg =
    app === "meta"
      ? "bg-[#0866FF] text-white"
      : "bg-black text-white"; // tiktok/threads 공식 블랙 타일

  return (
    <span
      className={cn("flex shrink-0 items-center justify-center rounded-[22%]", bg, className)}
      style={{ width: size, height: size }}
      aria-hidden
    >
      <Glyph className="size-[60%]" />
    </span>
  );
}
